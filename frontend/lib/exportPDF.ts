    pdf.setTextColor(147, 51, 234);
    pdf.text(exhibition.name, pageWidth / 2, yPosition, { align: 'center' });
    yPosition += 15;

    // Venue and Date
    pdf.setFontSize(14);
    pdf.setTextColor(100, 100, 100);
    pdf.text(exhibition.venue, pageWidth / 2, yPosition, { align: 'center' });
