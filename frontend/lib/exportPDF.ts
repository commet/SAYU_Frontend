      if (yPosition > pageHeight - 40) {
        pdf.addPage();
        yPosition = margin;
      }

      // Insight details
      pdf.setFontSize(14);
      pdf.setFont('helvetica', 'bold');
      pdf.text(`${insight.artwork}`, margin, yPosition);
      yPosition += 8;

      pdf.setFontSize(11);
